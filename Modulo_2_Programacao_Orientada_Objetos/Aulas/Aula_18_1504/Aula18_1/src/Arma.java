public class Arma
{
    String categoria;
    int danoDaArma;
    int distanciaDoAtaque;
    int resistenciaDaArma;
    public Arma(String categoria, int danoDaArma, int distanciaDoAtaque, int resistenciaDaArma)
    {
        this.categoria = categoria;
        this.danoDaArma = danoDaArma;
        this.distanciaDoAtaque = distanciaDoAtaque;
        this.resistenciaDaArma = resistenciaDaArma;
    }
}
