public class Monstro
{
    String drop;
    int nivel;
    int pontosDeVida;
    int pontosDeForca;
    int pontosDeDefesa;
    public Monstro(String drop, int nivel, int pontosDeVida, int pontosDeForca, int pontosDeDefesa)
    {
        this.drop = drop;
        this.nivel = nivel;
        this.pontosDeVida = pontosDeVida;
        this.pontosDeForca = pontosDeForca;
        this.pontosDeDefesa = pontosDeDefesa;
    }
}
