var numero1 = 5
var numero2 = 2
var numero3 = "5"
console.log(numero1 > numero2) //true, é maior
console.log(numero1 < numero2) //false, não é menor
console.log(numero1 >= numero2) //true, é maior ou igual
console.log(numero1 <= numero2) //false, não é menor ou igual
console.log(numero1 == numero2) //false, não é igual
console.log(numero1 == numero3) //true, é igual
console.log(numero1 === numero3) //false, não é identico